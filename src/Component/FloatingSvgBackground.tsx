function rotationMatrix(angleRad: number): string {
  const c = Math.cos(angleRad);
  const s = Math.sin(angleRad);
  return `matrix(${c},${s},${-s},${c},0,0)`;
}

const FloatingSvgBackground = ({
  className = "",
  offsetLeft = false,
}: {
  className?: string;
  /** On Consulenza/Public Policy: rotate & shift so seaweed starts from left, no gap */
  offsetLeft?: boolean;
}) => {
  const svgRef = useRef<SVGSVGElement>(null);
  const [isMobile, setIsMobile] = useState(() => {
    if (typeof window === "undefined") return false;
    return window.matchMedia("(max-width: 768px)").matches;
  });

  useEffect(() => {
    const media = window.matchMedia("(max-width: 768px)");
    const update = () => setIsMobile(media.matches);

    if (typeof media.addEventListener === "function") {
      media.addEventListener("change", update);
      return () => media.removeEventListener("change", update);
    }

    const legacy = media as unknown as {
      addListener?: (cb: () => void) => void;
      removeListener?: (cb: () => void) => void;
    };
    if (typeof legacy.addListener === "function") {
      legacy.addListener(update);
      return () => legacy.removeListener && legacy.removeListener(update);
    }

    return;
  }, []);

  useEffect(() => {
    const svg = svgRef.current;
    if (!svg) return;

    const groups = svg.querySelectorAll<SVGGElement>("g[data-seaweed-line]");
    if (groups.length === 0) return;

    let rafId: number;

    const tick = () => {
      const t = (performance.now() / 1000);
      const { amp, speed, delay } = SEAWEED_CONFIG;

      groups.forEach((g, i) => {
        const theta = amp * Math.sin(2 * Math.PI * speed * t + delay * i);
        g.setAttribute("transform", rotationMatrix(theta));
      });

      rafId = requestAnimationFrame(tick);
    };

    rafId = requestAnimationFrame(tick);
    return () => cancelAnimationFrame(rafId);
  }, []);

  return (
    <div
      className={`fixed inset-0 z-0 overflow-visible pointer-events-none md:absolute md:overflow-hidden ${className}`}
      aria-hidden
    >
      <svg
        ref={svgRef}
        xmlns="http://www.w3.org/2000/svg"
        viewBox="0 0 1690 837"
        preserveAspectRatio="xMidYMid meet"
        className="absolute w-full h-full object-cover seaweed-svg"
        style={{
          // Keep desktop positioning (offset/scale) so it appears consistently on smaller screens.
          minWidth: isMobile ? "138%" : "120%",
          minHeight: isMobile ? "138%" : "120%",
          left: offsetLeft ? "-20%" : "-20%",
          // Move the seaweed up on mobile by using a more negative top offset.
          top: offsetLeft ? (isMobile ? "-46%" : "-20%") : isMobile ? "-44%" : "-20%",
          transform: offsetLeft
            ? isMobile
              ? "rotate(5deg) scale(1.45)"
              : "rotate(5deg) scale(1.25)"
            : isMobile
              ? "rotate(5deg) scale(1.42)"
              : "rotate(5deg) scale(1.2)",
          transformOrigin: offsetLeft ? "left top" : "left top",
        }}
      >
        <defs>
          <linearGradient gradientUnits="objectBoundingBox" y2="1" y1="0" x2="0" x1="0" id="seaweed-lg">
            <stop offset="0" stopColor="#1c79c0" />
            <stop offset="0.5" stopColor="#fbff0d" />
            <stop offset="1" stopColor="#0389ff" />
          </linearGradient>
        </defs>
        {SEAWEED_GROUPS.map((group, i) => (
          <g key={i} data-seaweed-line>
            <path
              strokeWidth="0.5"
              stroke="url(#seaweed-lg)"
              fill="none"
              d={group.d}
            />
          </g>
        ))}
      </svg>
    </div>
  );
};

export default FloatingSvgBackground;
